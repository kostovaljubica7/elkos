function setupLanguage(language) {
    document.querySelectorAll('[data-key]').forEach(element => {
        const key = element.getAttribute('data-key'); // Get the key from the 'data-key' attribute
        if (translations[key]) {
            element.textContent = translations[key][language]; // Update content based on selected language
        }
        const logo = document.getElementById("logo");

        if (language === "en") {
            logo.src = "img/custom/logo-en-removebg.png";
        } else if (language === "mk") {
            logo.src = "img/custom/logo_green.png";
        }
        var selectElement = document.getElementById("language-select");
        selectElement.value = language;
    });
}

function setLanguage(language) {
    if(window.location.host == ''){  // this is for local use
        setupLanguage(language);
        return
    }
    
    let newUrl = window.location.origin;
    newUrl = origin + "/" + language;
    window.location.href = newUrl;
}

window.onload = function() {
    const path = window.location.pathname;
    const language = path.includes('/en') ? 'en' : 'mk';  // Default to 'mk' if not '/en'
    setupLanguage(language);
};
